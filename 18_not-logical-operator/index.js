// ! NOT logical operator - often used to reverse a boolean's condition value

let temp = 15;
let sunny = true;

if(!(temp > 0)) {
    console.log("It's cold outside");
} else {
    console.log("It's warm outside");
}

if(!sunny) {
    console.log("It's cloudy outside");
} else {
    console.log("It's sunny outside");
}